#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else if (n < 0 && n > -2147483648)
	{
		write (fd, "-", 1);
		n *= (-1);
	}
	if (n >= 0 && n <= 9)
	{
		c = n + 48;
		write (fd, &c, 1);
	}
	else if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
}
