#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))

{
	int	i;
	int	j;

	i = (int)ft_strlen(s);
	j = 0;
	while (j < i)
	{
		f(j, &s[j]);
		j ++;
	}
}
